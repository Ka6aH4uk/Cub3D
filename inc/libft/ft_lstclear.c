#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))

{
	t_list	*param;

	if (lst && del)
	{
		while (*lst)
		{
			param = (*lst)->next;
			ft_lstdelone(*lst, del);
			*lst = param;
		}
	}
}
