#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*result;

	i = 0;
	result = (char *)s;
	while (i < n)
	{
		result[i] = '\0';
		i++;
	}
	s = (void *)result;
}
